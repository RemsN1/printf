#include "libft.h"

void			ft_putendl_fd(char *s, int fd)
{
	size_t	i;

	if (!s)
		return ;
	i = 0;
	while (s[i])
		++i;
	if (s[0] != '\0')
		write(fd, s, i);
	write(fd, "\n", 1);
}
